#include <iostream>
#include <cmath> // Для математических функций

int main() {
    // Дано
    double m_n = 1.5;  // Погрешность начального дирекционного угла в секундах
    double m_b = 2.5;  // Погрешность измеренного угла в секундах
    int n = 7;         // Номер стороны

    // Формула для вычисления средней квадратической погрешности
    double m_7 = std::sqrt(std::pow(m_n, 2) + n * std::pow(m_b, 2));

    // Вывод результата
    std::cout << "Средняя квадратическая погрешность дирекционного угла 7-й стороны: "
              << m_7 << " сек" << std::endl;

    return 0;
}